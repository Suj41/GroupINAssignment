package yoho;

public class baau {
    private String name;
    private int roll;
    public baau(){

    }
    public baau(String name, int roll){
        this.name=name;
        this.roll=roll;
    }
    public String getName(){
        return name;
    }
    public int getRoll(){
        return roll;
    }
    public void setName(int roll){
        this.roll=roll;
    }

}
